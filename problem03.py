# Create a program that calculates and displays the arithmetic
# mean of three grades entered by the user

grande1 = float(input("enter the grade of first subject :"))
grande2 = float(input("enter the grade of second subject :"))
grande3 = float(input("enter the grade of third subject :"))

mean = (grande1+grande2+grande3)/3

print("mean of 3 subjects is :",mean)
